// String (ข้อความ)
let myString = "Hello, JavaScript!";
console.log("String:", myString);

// Integer (จำนวนเต็ม)
let myInteger = 42;
console.log("Integer:", myInteger);

// Boolean (ค่าความจริง)
let myBoolean = true;
console.log("Boolean:", myBoolean);

// แปลง String เป็น Integer
let strToInt = parseInt("10"); 
console.log("String to Integer:", strToInt);

// แปลง String เป็น Float
let strToFloat = parseFloat("3.14");
console.log("String to Float:", strToFloat);

// แปลง Number เป็น String
let numToStr = String(100);
console.log("Number to String:", numToStr);

// แปลง Boolean เป็น String
let boolToStr = String(true);
console.log("Boolean to String:", boolToStr);

// แปลง String เป็น Boolean
let strToBool = Boolean("true");
console.log("String to Boolean:", strToBool);
